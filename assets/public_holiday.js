// Note: Month Starts from 0, 0 = Jan
var publicHoliday = [new Date(2024,0,1),new Date(2024,0,26),new Date(2024,2,11),new Date(2024,2,29),new Date(2024,2,30),new Date(2024,2,31),new Date(2024,3,01),new Date(2024,3,25),new Date(2024,5,10),new Date(2024,10,5),new Date(2024,11,25),new Date(2024,11,26),new Date(2025,0,1)];
function isPublicHoliday(customDate){
  var isPublicHoliday = false;
  if (customDate == null){
    var checkDate = new Date();
  }
  else {
    checkDate = new Date(customDate.valueOf());
  }
  checkDate.setHours(0,0,0,0);
  publicHoliday.forEach(function(ph){
    if (ph.valueOf() === checkDate.valueOf()){
    	isPublicHoliday = true;
    }
  })
  return isPublicHoliday;
}

